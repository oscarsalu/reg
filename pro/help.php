    <h2>1.0 GENERAL INFORMATION</h2>
    <h3>1.1 System Overview</h3>
    <p>
     Smart registration system is a Web_based system,that provide a platform which anyone willing to join The Technical university of kenya can register follow the process and finally select a course which he/she want among the available options. In short its a university ragistration system which has digitize the whole registration process, in turn saving your time and the paper work involved in the same process.<br>
     1. User management <br>
     2. Submission management <br>
     3. Data entry <br>
     4. Key category analysis <br>
     5. Reporting tables <br>
     6. Data Export/Import <br>
     7. Completeness <br>
     8. Consistency <br>
     <br>
     <bold>System is a major application</bold> and not a general purpose system. 
     <br>
     Currently the system is under development,
     it can be used by mobile devices nad also desktop.
    </p>
    <p>
        The system shows the courses offered where one can download and pass through them to see the qualifications,it shows the available programs ,start date ,end date , academic year and course available for each program .
        You can search for any course and the qualifications will be diplayed .
        <br>
        Allows the student to upload his certificates,and provides each applicant a unique profile where he/she can view his details, insert his grades according to the certification he/she used.
        <br>
        it also performs an autoselection depending on the students grade and the set course grades, after it has determined u have qualified it sends you an invitational email
        </p>
